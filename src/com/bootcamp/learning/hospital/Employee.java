package com.bootcamp.learning.hospital;

public class Employee {
	private String name;
	private Integer age;
	public String getName() {
		return name;
	}
	public Employee setName(String name) {
		this.name = name;
		return this;}
	
	public Integer getAge() {
		return age;
	}
	public Employee setAge(Integer age) {
		this.age = age;
		return this;
	}
	
	
	
}
